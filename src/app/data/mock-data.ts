export interface User {
  id: string;
  name: string;
  email: string;
  role: 'admin' | 'project_manager' | 'foreman' | 'sales_rep';
  permissions: string[];
  isActive: boolean;
  createdAt: string;
}

export interface TeamMember {
  id: string;
  name: string;
  email: string;
  phone: string;
  role: 'admin' | 'project_manager' | 'foreman' | 'sales_rep';
  activeProjects: number;
}

export interface Invoice {
  id: string;
  projectId: string;
  clientId: string;
  invoiceNumber: string;
  invoiceDate: string;
  dueDate: string;
  status: 'draft' | 'sent' | 'paid' | 'overdue';
  items: {
    description: string;
    quantity: number;
    unitPrice: number;
    total: number;
  }[];
  subtotal: number;
  tax: number;
  total: number;
  amountPaid: number;
  amountDue: number;
}

export interface ChangeOrder {
  id: string;
  projectId: string;
  clientId: string;
  orderNumber: string;
  title: string;
  description: string;
  requestDate: string;
  status: 'pending' | 'approved' | 'rejected';
  originalAmount: number;
  changeAmount: number;
  newAmount: number;
  approvedBy?: string;
  approvedDate?: string;
}

export interface Contract {
  id: string;
  projectId: string;
  clientId: string;
  contractNumber: string;
  title: string;
  signedDate?: string;
  startDate: string;
  endDate: string;
  status: 'draft' | 'sent' | 'signed' | 'completed';
  totalValue: number;
  terms: string;
  documentUrl?: string;
}

export interface Payment {
  id: string;
  invoiceId: string;
  clientId: string;
  paymentDate: string;
  amount: number;
  method: 'credit_card' | 'ach' | 'check' | 'wire_transfer';
  status: 'pending' | 'completed' | 'failed';
  transactionId?: string;
  notes?: string;
}

